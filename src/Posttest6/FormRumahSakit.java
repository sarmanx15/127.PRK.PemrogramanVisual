    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:        
        if(tfNama.getText().length()==0||tfTanggal.getText().length()==0||
                tfUsia.getText().length()==0||lDiagnosa.isSelectionEmpty()==true ||
                tfDokter.getText().length()==0||tfRuangan.getText().length()==0||
                tfTanggalmasuk.getText().length()==0||(rbLaki.isSelected()==false && rbPerempuan.isSelected()==false)||
                !(tfTanggal.getText().length()==10)||!(tfTanggalmasuk.getText().length()==10)){
            
            JOptionPane.showMessageDialog(null, "Silahkan Isi Semua Data dengan Benar!","Konfirmasi",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            String jk = null;
            String nama_pasien = tfNama.getText();
            String tanggal = tfTanggal.getText();
            int usia = Integer.parseInt(tfUsia.getText());
            if(rbLaki.isSelected()){
                jk = "L";
            }
            else if(rbPerempuan.isSelected()){
                jk = "P";
            }
            
            String diagnosa = lDiagnosa.getSelectedValue();
            String dokter = tfDokter.getText();
            String ruangan = tfRuangan.getText();
            String tanggalmasuk = tfTanggal.getText();
            TambahData(nama_pasien,tanggal,usia,jk,diagnosa,dokter,ruangan,tanggalmasuk);
            JOptionPane.showMessageDialog(rootPane, "Data Berhasil Disimpan", "Perhatian", WIDTH);
            bersihkan();
        }
            
        
    }//GEN-LAST:event_btnSimpanActionPerformed

