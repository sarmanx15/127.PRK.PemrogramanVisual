    private void tfCariKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfCariKeyTyped
        // TODO add your handling code here:
        if(cbCari.getSelectedItem().equals("Cari Berdasarkan")){
             JOptionPane.showMessageDialog(null, "Pilih Filter Pencarian","Konfirmasi",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
        DefaultTableModel model1 = new DefaultTableModel();
        String cariapa;
        if(cbCari.getSelectedItem().toString()=="Pasien"){
            cariapa = "nama_pasien";
        }
        else{
            cariapa = "dokter";
        }
        try{
            String sql = "Select * from rumahsakit where "+cariapa+" like '%" + tfCari.getText() + "%'";
            stt = con.createStatement();
            rss = stt.executeQuery(sql);
            model.setRowCount(0);
            Object[] o = new Object[8];
            int x = 1;
            while(rss.next()){
                o[0] = rss.getString("nama_pasien");
                o[1] = rss.getDate("tl");
                o[2] = rss.getInt("usia");
                o[3] = rss.getString("jk");
                o[4] = rss.getString("diagnosa");
                o[5] = rss.getString("dokter");
                o[6] = rss.getString("ruangan");
                o[7] = rss.getDate("tgl_masuk");
                model.addRow(o);
                x++;
            }
            rss.close();
            stt.close();
                }catch (SQLException e){
                    System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_tfCariKeyTyped

