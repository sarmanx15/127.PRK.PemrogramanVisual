    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        Float bilangan1, bilangan2, hasil = null;
        bilangan1 = Float.parseFloat(tfBilangan1.getText());
        bilangan2 = Float.parseFloat(tfBilangan2.getText());
        if(rbTambah.isSelected()==true){
            hasil = bilangan1+bilangan2;
        }
        
        else if(rbKurang.isSelected()==true){
            hasil = bilangan1-bilangan2;
        }
        else if(rbKali.isSelected()==true){
            hasil = bilangan1*bilangan2;
        }
        else if(rbBagi.isSelected()==true){
            hasil = bilangan1/bilangan2;
        }
        else{
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Operasi Hitung");
        }
        
        tfHasil.setText(String.valueOf(hasil));
    
        
        
        
    }//GEN-LAST:event_btnHitungActionPerformed

