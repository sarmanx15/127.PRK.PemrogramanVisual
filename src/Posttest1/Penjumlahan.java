    private void tfBilangan1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfBilangan1KeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==evt.VK_ENTER){
               if(tfBilangan1.getText().length()==0){
                   JOptionPane.showMessageDialog(null, "Masukan Tidak Boleh Kosong","Konfirmasi",JOptionPane.INFORMATION_MESSAGE);
                   tfBilangan1.requestFocus();
               }
               else{
                   tfBilangan2.requestFocus();
               }
               
        }
    }//GEN-LAST:event_tfBilangan1KeyPressed

